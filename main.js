// Initialize app
document.addEventListener('DOMContentLoaded', function() {
    // Load saved preferences
    const savedLang = localStorage.getItem('selectedLanguage') || 'ur';
    const savedTheme = localStorage.getItem('selectedTheme') || 'green';
    
    // Apply saved settings
    applyLanguage(savedLang);
    applyTheme(savedTheme);
    
    // Initialize dropdowns
    initializeDropdowns();
    
    // Update time if clock exists
    if (document.getElementById('clock')) {
        updateTime();
        setInterval(updateTime, 1000);
    }
});

// Initialize dropdown functionality
function initializeDropdowns() {
    const dropdowns = document.querySelectorAll('.dropdown');
    
    dropdowns.forEach(dropdown => {
        const btn = dropdown.querySelector('.dropdown-btn');
        
        btn.addEventListener('click', function(e) {
            e.stopPropagation();
            
            // Close other dropdowns
            dropdowns.forEach(d => {
                if (d !== dropdown) {
                    d.classList.remove('active');
                }
            });
            
            // Toggle current dropdown
            dropdown.classList.toggle('active');
        });
    });
    
    // Close dropdowns when clicking outside
    document.addEventListener('click', function() {
        dropdowns.forEach(dropdown => {
            dropdown.classList.remove('active');
        });
    });
}

// Apply language
function applyLanguage(langCode) {
    const lang = translations[langCode];
    if (!lang) return;
    
    // Update all translatable elements
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (lang[key]) {
            element.textContent = lang[key];
        }
    });
    
    // Update HTML lang and dir attributes
    const isRTL = ['ur', 'ar', 'fa', 'ps'].includes(langCode);
    document.documentElement.setAttribute('lang', langCode);
    document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');
    
    // Save preference
    localStorage.setItem('selectedLanguage', langCode);
}

// Apply theme
function applyTheme(themeName) {
    // Remove all theme classes
    Object.keys(themeColors).forEach(theme => {
        document.body.classList.remove(`theme-${theme}`);
    });
    
    // Add selected theme class
    document.body.classList.add(`theme-${themeName}`);
    
    // Save preference
    localStorage.setItem('selectedTheme', themeName);
}

// Update time display
function updateTime() {
    const now = new Date();
    const hours = String(now.getHours()).padStart(2, '0');
    const minutes = String(now.getMinutes()).padStart(2, '0');
    const seconds = String(now.getSeconds()).padStart(2, '0');
    
    const clockElement = document.getElementById('clock');
    const dateElement = document.getElementById('date');
    
    if (clockElement) {
        clockElement.textContent = `${hours}:${minutes}:${seconds}`;
    }
    
    if (dateElement) {
        const options = { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' };
        dateElement.textContent = now.toLocaleDateString('ur-PK', options);
    }
}

